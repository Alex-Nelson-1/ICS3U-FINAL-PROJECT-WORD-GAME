// This program displays a rectangles area and perimeter

#include <stdio.h>

int main() {
    // this function displays a rectangles area and perimeter

    printf("If a rectangle has the dimensions:\n");
    printf("5 cm × 3 cm");
    printf("\n\nArea is %.3f cm².", (5.0 * 3));
    printf("\nPerimeter is %.3f cm.", (2.0 *(5.0 + 3)));

    printf("\n\nDone.\n");
    return 0;
}
